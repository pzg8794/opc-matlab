;ADNER RAMOS
;BINARY TO BCD 
;USE REPEATED DIVISION BY 10


DATA SEGMENT
DATA_1 	DW	FFFF, 1999, 028F, 0041, 0006,0000

DIGIT DB 5 DUP (?)

DATA ENDS


CODE SEGMENT

ASSUME CS:CODE,DS:DATA

BINBCD: MOV AX, DATA
	MOV DS, AX
	LEA BX, DATA_1
	MOV CX, 10
AGAIN:	MOV AX, BX
	DIV CX
	MOV [DI],DL
	INC BX
	JNZ AGAIN
	RET
CODE ENDS
END BINBCD