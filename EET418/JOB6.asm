; PAIRS OF ONES
; PAIRS OF ONE IN REG. AX
; ANSWER IS RETURNED IN AX REG.

	CODE SEGMENT
	ASSUME CS: CODE
PAIR:	MOV BX,AX
	XOR CX,CX
ZERO:	AND BX,BX
	JZ END
	SHL BX,1
	JNC ZERO
	JNS ZERO
	INC CX
	JMP ZERO
END:	RET


 CODE ENDS
 END PAIR
 END