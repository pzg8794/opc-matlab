;GENERATE FIBONACI SERIES
;0,1,1,2,3,5,8,13,21,43.......
;16 BIT ANSWERS IN AX AND BX (in HEX)

CODE SEGMENT

    ASSUME CS:CODE
         
FIBO:   XOR AX,AX
	XOR CX,CX
	MOV BX,01
	MOV CX,AX
AGAIN:  ADD CX,BX
	MOV BX,AX
	MOV AX,CX
        JMP AGAIN
        
CODE ENDS
END FIBO
    END