;STEEVE BISSERETH
;LAB 20

AVERAGE MACRO RESULT
     LOCAL AGAIN

	MOV CX,16
AGAIN:  ADD DL,[SI]
	ADC DH,00
	INC SI
	LOOP AGAIN
	MOV CL,04
	SHR DX,CL

ENDM

DATA SEGMENT
       BLOCK1   DB  16 DUP (?)
       BLOCK2   DB  16 DUP (?)
       BLOCK3   DB  16 DUP (?)
DATA ENDS

CODE SEGMENT

    ASSUME DS:DATA,CS:CODE
    

MAIN:   MOV AX,DATA
	MOV DS,AX
        XOR AX,AX
	XOR BX,BX
	XOR CX,CX
	XOR DX,DX
        LEA SI,BLOCK1
        AVERAGE
	SHL DX,1
	MOV BX,DX
	XOR DX,DX
	LEA SI,BLOCK2
	AVERAGE
	MOV AX,DX
	XOR DX,DX
	LEA SI,BLOCK3
	AVERAGE
	ADD AX,BX
	ADD AX,DX
	MOV CL,02
	SHR AX,CL
	MOV BX,AX
	
	RET

CODE ENDS
END MAIN