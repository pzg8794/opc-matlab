DATA SEGMENT
	BIT_WEIGHT	DW	1
			DW	2
			DW	4
			DW	8
			DW	10
			DW	20
			DW	40
			DW	80
			DW	100
			DW	200
			DW	400
			DW	800
			DW	1000
			DW	2000
			DW	4000
			DW	8000
	DATA ENDS

CODE SEGMENT
	
	ASSUME CS:CODE,DS:DATA

BCDBIN:	MOV	BX,AX
	MOV	AX,DATA
	MOV	DS,AX
	LEA	DX,BIT_WEIGHT
	AND	AX,0
	MOV	CL,16
	SHL	BX,1
	JNC	NOBIT
	ADD	AX,[DX]
NOBIT:	INC	DX
	DEC	CL
	JNZ	AGAIN
	RET

CODE ENDS
END BCDBIN
