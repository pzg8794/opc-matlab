;Keith Manta
;Volume of Box = X*Y*Z part 1
DATA SEGMENT
	X	DB	0ABH
	Y	DB	0CDH
	Z	DB	0EFH
DATA ENDS

CODE SEGMENT
	
	ASSUME CS:CODE,DS:DATA

VOLUME:	MOV	AX,DATA
	MOV	DS,AX
	AND	AX,0
	MOV	AL,X
	MOV	BL,Y
	MUL	BL
	MOV	BX,AX
	MOV	AL,Z
	MOV	AH,0
	MUL	BX
	HLT
CODE ENDS
END VOLUME

