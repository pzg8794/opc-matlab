;BINARY TO BCD REPEATED DIVISION BY 10

DATA SEGMENT
		
	DIGIT DB 5 DUP (?)	

	DATA ENDS

CODE SEGMENT
	
	ASSUME CS:CODE,DS:DATA

BINBCD:	MOV BX,DATA
	MOV DS,BX
	LEA DI,DIGIT
	MOV BX,10
	MOV CX,5
AGAIN:	XOR DX,DX
	DIV BX
	MOV [DI],DX
	INC DI
	LOOP AGAIN
	RET

CODE ENDS
END BINBCD
