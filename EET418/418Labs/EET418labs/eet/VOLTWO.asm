;Keith Manta
;Volume of Box = X*Y*Z part 2
DATA SEGMENT
	X	DW	0ABCDH
	Y	DW	0BCDEH
	Z	DW	0CDEFH
DATA ENDS

CODE SEGMENT
	
	ASSUME CS:CODE,DS:DATA

VOLUME:	MOV	AX,DATA
	MOV	DS,AX
	MOV	AX,X
	MOV	BX,Y
	MUL	BX
	MOV	CX,DX
	MUL	Z
	MOV	DI,AX
	MOV	BX,DX
	MOV	AX,CX
	MUL	Z
	ADD	BX,AX
	ADC	DX,0
	MOV	SI,BX
	MOV	BP,DX
	HLT
CODE ENDS
END VOLUME

