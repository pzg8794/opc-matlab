;find low

DATA SEGMENT
	NUMBER	DB 99
		DB 68
		DB 82
		DB 66
		DB 100

DATA ENDS


CODE SEGMENT
	
	ASSUME CS:CODE, DS:DATA

FINDLW:	MOV	AX,DATA
	MOV	DS,AX
	AND	AX,0
	LEA	BX,NUMBER
	MOV	AL,[BX]
	MOV	CL,4
AGAIN:	INC	BX
	MOV	DL,[BX]
	CMP	AL,DL
	JLE	SKIP
	MOV	AL,DL
SKIP:	DEC	CL
	JNZ	AGAIN
	RET
	

CODE ENDS
END FINDLW
	END
