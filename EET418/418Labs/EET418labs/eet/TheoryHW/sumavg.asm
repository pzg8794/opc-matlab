;sum and average... AVG in AX, Sum in DX

DATA SEGMENT
	NUMBER	DB 99
		DB 68
		DB 82
		DB 66
		DB 100

DATA ENDS


CODE SEGMENT
	
	ASSUME CS:CODE, DS:DATA

SUMAVG:	MOV	AX,DATA
	MOV	DS,AX
	AND	AX,0
	MOV	CL,5
	LEA	BX,NUMBER
AGAIN:	ADD	AL,[BX]
	ADC	AH,0
	INC	BX
	DEC	CL
	JNZ	AGAIN

	
	MOV DX, AX
	MOV CH, 5
	DIV CH
	RET
	

CODE ENDS
END SUMAVG
	END
