;EBCDIC to ASCII... ASCII in BL, EBCDIC in AL

DATA SEGMENT
	EBCDIX	DB 0F0H,0F1H,0F2H,0F3H,0F4H,0F5H,0F6H
		DB 0F7H,0F8H,0F9H
		DB 0C1H,0C2H,0C3H,0C4H,0C5H,0C6H,0C7H
		DB 0C8H,0C9H
		DB 0D1H,0D2H,0D3H,0D4H,0D5H,0D6H,0D7H
		DB 0D8H,0D9H
		DB 0E2H,0E3H,0E4H,0E5H,0E6H,0E7H,0E8H,
		DB 0E9H

	ASCII	DB '0123456789'
		DB 'ABCDEFGHI'
		DB 'JKLMNOPQR'
		DB 'STUVWXYZ'	
	DATA ENDS


CODE SEGMENT
	
	ASSUME CS:CODE,DS:DATA

CONV:	MOV	DX,DATA
	MOV	DS,DX
	LEA	SI,EBCDIC
	LEA	DI,ASCII
	MOV	CL,36
AGAIN:	CMP	AL,[SI]
	JE	DONE
	INC	SI
	INC	DI
	DEC	CL
	JNZ	AGAIN
	MOV	BL,0
	JMP	FINISH
DONE:	MOV	BL,[DI]
FINISH:	RET

CODE ENDS
END CONV
	END
