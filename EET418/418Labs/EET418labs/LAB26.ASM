;STEEVE BISSERETH
;SUBROUTINE TO READ & DECODE KEYBOARD
;MOVING "0" IS OUTPUT ON PORT A.
;PROGRAM WAITS FOR A BIT TO GO
;LOW ON PORT B.
;OUTPUT CODE IS SAVED IN BH.
;INPUT CODE IS RETURNED IN BL.
;BX IS DECODED & ASCII CODE RETURNED IN DL.

DATA SEGMENT
	TABLE1	DW	0BFDFH,0EFF7H,0DFF7H,0DFDFH
		DW	0BFFDH,0DFFBH,0EFDFH,0EFFBH
		DW	0F7BFH,0F7DFH,0F7FBH,0FBDFH
		DW	0F7F7H,0F7EFH,0F7FDH,0FBBFH
		DW	07FFDH,0DFBFH,0BFFBH,0DFFDH
		DW	0EFFDH,0EFEFH,0BFBFH,0DFEFH
		DW	0EFBFH,0BFF7H

		DW	0F7FEH,07F7FH,07FFEH,0BF7FH
		DW	0BFFEH,0DF7FH,0DFFEH,0EF7FH
		DW	0EFFEH,0F77FH

	TABLE2	DB	'A','B','C','D','E','F','G'

		DB	'H','I','J','K','L','M','N'

		DB	'O','P','Q','R','S','T','U'

		DB	'V','W','X','Y','Z'

		DB	'0','1','2','3','4'
		DB	'5','6','7','8','9'
DATA ENDS
