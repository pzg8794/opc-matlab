;ADNER RAMOS
;LAB 9
;CODE CONVERSION PROGRAM EBCDIC IN AL.
;ASCII RETURNED IN BL.
;USE SI AND DI AS POINTERS
;RETURNS 0 FOR AN INVALID INPUT CODE


DATA SEGMENT
     EBCDIC  DB 0F0H,0F1H,0F2H,0F3H,0F4H,0F5H
             DB 0F6H,0F7H,0F8H,0F9H
             DB 0C1H,0C2H,0C3H,0C4H,0C5H,0C6H
             DB 0C7H,0C8H,0C9H
             DB 0D1H,0D2H,0D3H,0D4H,0D5H,0D6H
             DB 0D7H,0D8H,0D9H
             DB 0E2H,0E3H,0E4H,0E5H,0E6H,0E7H
             DB 0E8H,0E9H
        
     ASCII   DB '0123456789'
             DB 'ABCDEFGHI'
             DB 'JKLMNOPQR'
             DB 'STUVWXYZ'
DATA ENDS

CODE SEGMENT

ASSUME DS:DATA,CS:CODE

CONVERT:MOV	DX,AX	;STORE VALUE IN CX
	MOV	AX,DATA	
	MOV	DS,AX	;SET-UP

	LEA	DI,ASCII
	LEA	SI,EBCDIC
	MOV	AX,DX	;OBTAIN VALUE RESTORED IN CX
	MOV	CX, 39
AGAIN:	CMP	AL,[SI]
	JE	NEXT
	INC	SI	;INCREMENTING
	INC	DI
	LOOP	AGAIN
	MOV	BL,00
	JMP 	DONE
NEXT:	MOV	BL,[DI]
DONE:	HLT

CODE ENDS
END CONVERT
END

