;STEEVE BISSERETH
;16 BIT SEQUENCE GENERATOR IN AX
;LAB 19 PART A
;TAPS 1,3,12,16
;DETERMINES IF IT PRODUCES
;      65535 STATES

CODE SEGMENT

	ASSUME CS:CODE

SEQ16:	XOR CX,CX
        MOV AX,8000H
AGAIN:	INC CX
	MOV BX,AX
	AND BX,0A011H
	OR  BL,BH
	JPE SKIP
	STC
SKIP:	RCR AX,1
	CMP AX,8000H
	JNE AGAIN
	HLT

CODE ENDS
END SEQ16